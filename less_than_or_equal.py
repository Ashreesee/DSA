def less_than_or_equal_to_zero(num) :
    if num <= 0:
        return True
    else :
        return False
    
num = 4 
condition = less_than_or_equal_to_zero(num)
print(condition)

num = 0 
condition = less_than_or_equal_to_zero(num)
print(condition)

num = -3 
condition = less_than_or_equal_to_zero(num)
print(condition)


